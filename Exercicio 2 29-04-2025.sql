CREATE TABLE Planos_Assinatura (
    id INT PRIMARY KEY,
    nome_plano VARCHAR(50) NOT NULL,
    preco_mensal DECIMAL(5,2) NOT NULL,
    resolucao_maxima VARCHAR(20),
    numero_telas INT,
    suporte_download BOOLEAN DEFAULT FALSE 
);

CREATE TABLE Usuarios (
    id INT PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    telefone VARCHAR(20),
    data_cadastro DATE,
    plano_assinatura_id INT,
    idioma_preferido VARCHAR(30), -- campo adicional
    FOREIGN KEY (plano_assinatura_id) REFERENCES Planos_Assinatura(id)
);

CREATE TABLE Conteudos (
    id INT PRIMARY KEY,
    titulo VARCHAR(150) NOT NULL,
    tipo VARCHAR(30) CHECK (tipo IN ('filme', 'série', 'documentário')),
    genero VARCHAR(50),
    ano_lancamento INT CHECK (ano_lancamento >= 1900),
    duracao_minutos INT,
    classificacao_etaria VARCHAR(10),
    status_disponibilidade VARCHAR(20),
    idioma_original VARCHAR(30) -- campo adicional
);

CREATE TABLE Historico_Visualizacao (
    id INT PRIMARY KEY,
    id_usuario INT,
    id_conteudo INT,
    data_visualizacao DATE,
    progresso_percentual DECIMAL(5,2) CHECK (progresso_percentual BETWEEN 0 AND 100),
    dispositivo VARCHAR(30), -- campo adicional
    FOREIGN KEY (id_usuario) REFERENCES Usuarios(id),
    FOREIGN KEY (id_conteudo) REFERENCES Conteudos(id)
);

CREATE TABLE Avaliacoes (
    id INT PRIMARY KEY,
    id_usuario INT,
    id_conteudo INT,
    nota INT CHECK (nota BETWEEN 0 AND 5),
    comentario TEXT,
    data_avaliacao DATE,
    publico BOOLEAN DEFAULT TRUE, -- campo adicional
    FOREIGN KEY (id_usuario) REFERENCES Usuarios(id),
    FOREIGN KEY (id_conteudo) REFERENCES Conteudos(id)
);



